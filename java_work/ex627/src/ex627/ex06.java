package ex627;

public class ex06 {
	
	public static void main(String[] args) {
		
		int n = 5;

		switch (n) {
		case 1:
			System.out.println("n == 1");
			break;
		case 2:
			System.out.println("n == 2");
			break;
		case 3:
			System.out.println("n == 3");
			break;
		case 4:
			System.out.println("n == 4");
			break;

		default:
			System.out.println("n does not match with case number");
			break;
		}
		System.out.println("끝");
	}
}
